int	ft_acmp_int(void *int1, void *int2)
{
	int	nb1;
	int	nb2;

	nb1 = **(int **)int1;
	nb2 = **(int **)int2;
	if (nb1 == nb2)
		return (0);
	if (nb1 > nb2)
		return (1);
	return (-1);
}
